    private void btnMasActionPerformed(java.awt.event.ActionEvent evt) {                                       
        // TODO add your handling code here:
        DefaultTableModel temp = (DefaultTableModel) jTableProcesos.getModel();
        Object nuevo[] = {temp.getRowCount() + 1, "", ""};
        temp.addRow(nuevo);
    }                                      

    private void btnMenosActionPerformed(java.awt.event.ActionEvent evt) {                                         
        // TODO add your handling code here:
        try {
            DefaultTableModel temp = (DefaultTableModel) jTableProcesos.getModel();
            temp.removeRow(temp.getRowCount() - 1);
        } catch (ArrayIndexOutOfBoundsException e) {
        }
    }                                        

    private void txtElementosActionPerformed(java.awt.event.ActionEvent evt) {                                             
        // TODO add your handling code here:
        String ele = txtElementos.getText();
        for (int i = 0; i < jTableProcesos.getRowCount(); i++) {
            if (jTableProcesos.getValueAt(i, 3).equals(ele)) {
                jTableProcesos.changeSelection(i, 3, false, false);
                break;
            }
        }
    }                                            

